<h2 class="contentheading">
	Meldpunt Noord, Midden, Oost, West en Zuid-Nederlanders</h2>


<p>Sinds 22 februari 2006 is er sprake van hufterigheid en onbenul in de Nederlandse politiek. Op dit moment lopen de schattingen naar het aantal mensen uit deze landen, dat in Nederland de politiek te kijken zet, uiteen van 24 tot 38 personen. </p>

<p>Deze massale arbeidsimmigratie leidt tot veel problemen; overlast, vervuiling, verdringing op de arbeidsmarkt en integratie- en huisvestingsproblemen. Voor veel mensen vormen deze zaken een serieus probleem. Klachten worden echter vaak niet gemeld, omdat men het idee heeft dat er toch niets mee wordt gedaan.</p>

<p>Heeft u overlast van MOE-landers? Of bent u uw baan kwijtgeraakt aan een Pool, Bulgaar, Roemeen of andere Midden- of Oost Europeaan? Wij willen het graag horen. De Partij voor de Vrijheid biedt u op deze website een platform om uw klachten te melden. Deze klachten zullen wij in kaart brengen en de resultaten daarvan aanbieden aan de minister van Sociale Zaken en Werkgelegenheid.</p>

