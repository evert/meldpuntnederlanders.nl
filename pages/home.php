eigenlijk te goed voelen om ze te doen. Werk waar je de handen voor vuil moet
maken, dat is ten slotte ook te min voor zelfs de meest uitkeringsgerechtigde
Nederlander. Maar dat doet er niet toe, het betreft tenslotte buitenlanders. En
dat is een tot overlast, criminaliteit en dronkenschap neigend irritant volkje
