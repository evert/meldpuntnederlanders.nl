    Meldpunt Noord, Midden, Oost, West en Zuid-Nederlanders</h2>


<p>Sinds 22 februari 2006 is er sprake van een vrijbrief voor hufterigheid,
hypocrisie en verbaal geweld in de Nederlandse politiek. Feiten en logica maken
plaats voor chauvinisme en opruiende taal. Want Nederland heeft een unieke
cultuur en die moet beschermd worden. Zelfs als je daarvoor de redelijkheid en
het fatsoen moet overschrijden.</p>


