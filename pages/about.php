<h2 class="contentheading">Over ons</h2>

<p>
Onze website is in het leven geroepen als parodie op, door het PVV opgezette:
</p>

<p>
http://meldpuntmiddenenoosteuropeanen.nl/
</p>

<p>
Via deze website kunnen Nederlanders meldingen doen van situaties waar mensen uit de zogenaamde MOE-landen niet volgens nederlandse standaarden zich gedragen hebben.
</p>

<p>
Het 'demoniseren' (shit, is hier niet een ander woord voor?) van één bevolkingsgroep is iets wat ons erg tegen staat. Als er inderdaad sprake is van een hogere criminaliteit onder bevolkingsgroepen van deze 'MOE'-landen, dan moet er een betere manier zijn om dat te onderzoeken dan een publieke enquete die onder andere vraagt of er wel eens sprake is geweest van een situatie met een dronken pool. 
</p>

<p>
Wat voor antwoorden vallen daar nu uit te halen? Hoe vergelijkt dit met dronkenschap en associaal gedrag van limburgers? Wij zijn van mening dat dit statistisch weinig tot niets oplevert, en in eerste instantie alleen bedoeld is om afstand en respectloosheid te creeëren tussen Nederlanders en onze vrienden uit het verre oosten.
</p>

<p>
Met deze parodie website willen wij graag Nederlanders een spiegel voor houden. Wat zijn situaties die jij hebt gezien van intolerantie, het 'over 1 kam scheren' of huftigerigheid?
</p>

